import turtle
import snake
def clickcolor(x, y):
    print(x, y)
    if x <= 620 and x > 600 and y >=330 and y < 350:
        snake.pengreen()
    elif x <= 620 and x > 600 and y <= 320 and y > 300:
        snake.penblue()
    elif x <= 590 and x > 570 and y >=330 and y < 350:
        snake.penred()
    elif x <= 590 and x > 570 and y <= 320 and y > 300:
        snake.penyellow()
    elif x >= -610 and x < -590 and y <= 320 and y > 300:
        snake.pen.pensize(3)
    elif x >= -580 and x < -560 and y <= 330 and y > 300:
        snake.pen.pensize(4)
    elif x >= -550 and x < -510 and y <= 340 and y > 300:
        snake.pen.pensize(7)
    elif x >= -500 and x < -450 and y <= 360 and y > 300:
        snake.pen.pensize(10)
sc2 = turtle.Screen()
sc2.title('Paint for my friends(from Sergey Ledovskikh)')
sc2.setup(1280, 720)
sc2.onclick(clickcolor)
i = turtle.Turtle()
i.up()
i.goto(-195, 320)
i.down()
i.write("Нажмите на кнопку 'c' для очистки простраства для рисования\n(Английская раскладка)", font=("Verdana", 8))
i.speed(10)
i.up()
i.goto(600, 330)
i.down()
